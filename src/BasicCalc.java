
public class BasicCalc {
	static void add(int a,int b) {
		System.out.println(a+b);
	}
	static void multi(int a,int b) {
		System.out.println(a*b);
	}
	static void div(int a,int b) {
		System.out.println(a/b);
	}

	public static void main(String[] args) {
		add(10,20);
		multi(10,20);
		div(20,10);
	}

}
